    private void GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarActionPerformed
       String nombre = txtNombre.getText();
       String apellido = txtApellido.getText();
       String email = txtEmail.getText();
       String tema = txtTema.getText();
       
        oradorControl.guardar(nombre,apellido,email,tema);
        
        JOptionPane optionPane = new JOptionPane("Agregador Correctamente");
        optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        JDialog dialog = optionPane.createDialog("Titulo");
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
        OradorView oraView = new OradorView();
        oraView.setVisible(true);
        oraView.setLocationRelativeTo(null);
        this.dispose();
        
    }//GEN-LAST:event_GuardarActionPerformed

